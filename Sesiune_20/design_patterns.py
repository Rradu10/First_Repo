''' Design patterns (sabloane de proiectare) sunt niste solutii tipice la probleme comune in proiectarea software
Fiecare sablon este ca un model care poate fi personalizat pentru a rezolva o anumita problema de design din cod.
Ele definesc un limbaj comun care ajuta echipe de dezvoltare sa comunice mai eficient
Sabloanele difera prin complexitatea lor, nivelul de detalii si scala de aplicabilitate asupra intregului sistem proiect'''

''' Tipuri de design pattern:
1-Creationale (ofera mecanisme de creare a obiectelor care maresc flexibilitatea si reutilizarea codului existent
2-Structurale(definesc cum sa fie asamblate obiectele si clasele in structuri mai mari pastrand in acelasi timp flexibilitatea si eficienta lor
3-Comportamentale/Behavioral(se ocupa de comunicarea eficienta intre obiecte si de repartizarea responsabilitatilor intre acestea'''